    public void getDetails(){
        
        int BookId=Integer.parseInt(txt_bookid.getText());
        int StuId=Integer.parseInt(txt_stuid.getText());
         String url="jdbc:oracle:thin:@localhost:1523:orcl";
         
         
         
         try {
            Connection con=DriverManager.getConnection(url,"yosef","123456");
            String sql="SELECT * FROM student_book WHERE stu_id=? and book_ID=? and status =?";
            con.setAutoCommit(false);
            PreparedStatement pst=con.prepareStatement(sql);
            
             pst.setInt(1, StuId);
            pst.setInt(2, BookId);
            pst.setString(3, "Pending");
            
            
            
            
            ResultSet rs= pst.executeQuery();
            
            if(rs.next()){
              lbl_bookid.setText(rs.getString("Book_name"));
              lbl_stuid.setText(rs.getString("stu_name"));
              lbl_borroweddate.setText(rs.getString("Borrowed_date"));
              lbl_returneddate.setText(rs.getString("returned_date"));
             
                
            }
            else{
                lbl_bookid.setText("");
              lbl_stuid.setText("");
              lbl_borroweddate.setText("");
              lbl_returneddate.setText("");
                JOptionPane.showMessageDialog(this, "No Record Found");
                
            }
            
            con.commit();
            con.close();
            
             
             
             
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
    
