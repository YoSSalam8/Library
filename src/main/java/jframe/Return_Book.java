      public boolean ReturnBook(){
          
          boolean isReturned=false;
          int BookID=Integer.parseInt(txt_bookid.getText()) ;
          int StuId=Integer.parseInt(txt_stuid.getText());
           String url="jdbc:oracle:thin:@localhost:1523:orcl";
         
          try {
            Connection con=DriverManager.getConnection(url,"yosef","123456");
            String sql="Update student_book set status=? where  book_ID=? and stu_id=? and status =?";
            con.setAutoCommit(false);
            PreparedStatement pst=con.prepareStatement(sql);
            
            pst.setString(1, "Returned");
            pst.setInt(2, BookID);
            pst.setInt(3, StuId);
            pst.setString(4, "Pending");
            
            
            int RowCount=pst.executeUpdate();
            if(RowCount>0){
                isReturned=true;
            }
            else{
                isReturned=false;
            }
            
            con.commit();
            con.close();
              
              
              
              
              
          } catch (Exception e) {
              e.printStackTrace();;
          }
          return isReturned;
          
          
          
      } 
      
      
        
        
        
        
        
    

